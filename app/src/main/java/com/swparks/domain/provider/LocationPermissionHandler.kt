package com.swparks.domain.provider

/**
 * Интерфейс для обработки запросов и проверки разрешений геолокации.
 *
 * Используется в Compose UI для запроса разрешений геолокации
 * без прямой зависимости от Android Activity.
 */
interface LocationPermissionHandler {

    /**
     * Проверяет, предоставлено ли разрешение на геолокацию.
     *
     * @return true если разрешение предоставлено, false в противном случае
     */
    fun isGranted(): Boolean

    /**
     * Проверяет, было ли разрешение отклонено навсегда (forever denied).
     *
     * После вызова этого метода состояние "отклонено навсегда" сбрасывается,
     * чтобы корректно обработать следующий запрос разрешения.
     *
     * @return true если разрешение отклонено навсегда
     */
    fun isDeniedForever(): Boolean
}